import { NextRequest, NextResponse } from 'next/server';
import { getAdminClient } from '@/lib/supabase-admin';
import { authenticateUser, authErrorResponse } from '@/middleware/auth';
import { z } from 'zod';

// =====================================================
// INTERFACES E TIPOS
// =====================================================

interface AuthenticatedUser {
  user_id: string;
  email: string;
  role: string;
  bar_id?: number;
  [key: string]: any;
}

interface Execucao {
  id: string;
  checklist_id: string;
  funcionario_id: string;
  iniciado_por: string;
  status: string;
  iniciado_em: string;
  concluido_em?: string;
  respostas: any;
  observacoes?: string;
  progresso?: number;
  checklist?: {
    id: string;
    nome: string;
    setor: string;
    tipo: string;
    tempo_estimado: number;
    estrutura: any;
  };
  funcionario?: {
    id: string;
    nome: string;
    email: string;
  };
  iniciado_por_usuario?: {
    id: string;
    nome: string;
    email: string;
  };
}

interface ItemResposta {
  item_id: string;
  valor: any;
  anexos?: Array<{
    url: string;
    nome: string;
    tipo: string;
    tamanho?: number;
  }>;
  respondido: boolean;
  respondido_em?: string;
}

interface SecaoResposta {
  secao_id: string;
  itens: ItemResposta[];
}

interface Respostas {
  secoes: SecaoResposta[];
}

// =====================================================
// SCHEMAS DE VALIDAﾃﾃグ
// =====================================================

const SalvarRespostasSchema = z.object({
  respostas: z.object({
    secoes: z.array(
      z.object({
        secao_id: z.string(),
        itens: z.array(
          z.object({
            item_id: z.string(),
            valor: z.any(),
            anexos: z
              .array(
                z.object({
                  url: z.string(),
                  nome: z.string(),
                  tipo: z.string(),
                  tamanho: z.number().optional(),
                })
              )
              .optional(),
            respondido: z.boolean(),
            respondido_em: z.string().optional(),
          })
        ),
      })
    ),
  }),
  observacoes: z.string().optional(),
  auto_save: z.boolean().optional().default(false),
});

